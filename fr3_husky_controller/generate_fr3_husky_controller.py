    print(f"\nController class : fr3_husky_controller::{class_name}")
    print(f"Plugin type      : fr3_husky_controller/{class_name}")
    print(f"YAML controllers : left_{snake}, right_{snake}, dual_{snake}")
    print(f"Control mode     : {args.control_mode}")
    print("\nNext steps:")
    print("  1) Fill the TODO in update() with your control algorithm")
