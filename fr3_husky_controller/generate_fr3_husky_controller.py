"""
generate_fr3_husky_controller.py

Generates FR3+Husky combined controller boilerplate:
  - HPP: include/fr3_husky_controller/<snake>.hpp
  - CPP: src/<snake>.cpp

Also patches:
  - CMakeLists.txt             : adds .cpp to add_library block
  - fr3_husky_plugin.xml       : adds class entry
  - config/fr3_husky_ros_controllers.yaml : adds left_<name>, right_<name>, dual_<name>
                                            with manipulator_command_interface = <control_mode>

Usage:
  python3 generate_fr3_husky_controller.py MyWholebody --control_mode effort
  python3 generate_fr3_husky_controller.py MyWholebody --control_mode position --force
"""

