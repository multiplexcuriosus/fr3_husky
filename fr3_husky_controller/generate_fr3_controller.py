def main() -> None:
    ap = argparse.ArgumentParser(
        description="Generate FR3 controller boilerplate."
    )
    ap.add_argument("name", help="Server name (any style). Used for file/class names.")
    ap.add_argument(
        "--control_mode",
        type=str,
        required=True,
        choices=["position", "velocity", "effort"],
        help="Control Mode [position, velocity, effort]",
    )
    ap.add_argument(
        "--force",
        action="store_true",
        help="Overwrite existing files.",
    )
    args = ap.parse_args()

    pkg_root  = script_pkg_root()
    cmake_path = pkg_root / "CMakeLists.txt"
    xml_path   = pkg_root / "fr3_husky_plugin.xml"
    yaml_path  = pkg_root / "config" / "fr3_ros_controllers.yaml"

    for path in (cmake_path, xml_path, yaml_path):
        if not path.exists():
            raise FileNotFoundError(f"Required file not found: {path}")

    snake      = to_snake(args.name)
    class_name = to_camel(snake)

    hpp_path = pkg_root / "include" / "fr3_husky_controller" / f"{snake}.hpp"
    cpp_path = pkg_root / "src" / f"{snake}.cpp"

    if (hpp_path.exists() or cpp_path.exists()) and not args.force:
        raise FileExistsError(
            f"Target file(s) exist:\n  {hpp_path}\n  {cpp_path}\nUse --force to overwrite."
        )

    ensure_dir(hpp_path.parent)
    ensure_dir(cpp_path.parent)

    hpp_path.write_text(render_hpp(class_name), encoding="utf-8")
    cpp_path.write_text(render_cpp(class_name, snake), encoding="utf-8")

    cmake_changed, cmake_msg   = patch_cmakelists(cmake_path, f"src/{snake}.cpp")
    xml_changed,   xml_msg     = patch_plugin_xml(xml_path, class_name)
    yaml_changed,  yaml_msg    = patch_yaml(yaml_path, snake, class_name, args.control_mode)

    print("Generated:")
    print(f"  - {hpp_path.relative_to(pkg_root)}")
    print(f"  - {cpp_path.relative_to(pkg_root)}")
    print("CMakeLists.txt:")
    print(f"  - {cmake_msg}")
