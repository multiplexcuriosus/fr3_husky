def patch_plugin_xml(xml_path: Path, class_name: str) -> Tuple[bool, str]:
    """Add a <class> entry for class_name. Deduped."""
    text = xml_path.read_text(encoding="utf-8")
    full_class = f"fr3_husky_controller/{class_name}"

    if full_class in text:
        return False, f"Already present: {full_class}"

    new_entry = (
        f'\n  <class name="fr3_husky_controller/{class_name}"\n'
        f'         type="fr3_husky_controller::{class_name}"\n'
        f'         base_class_type="controller_interface::ControllerInterface">\n'
        f'    <description>\n'
        f'      {class_name} controller.\n'
        f'    </description>\n'
        f'  </class>\n'
    )

    # Insert before </library>
    insertion_point = text.rfind("</library>")
    if insertion_point == -1:
        xml_path.write_text(text + new_entry, encoding="utf-8")
        return True, "No </library> tag found; appended entry."

    updated = text[:insertion_point] + new_entry + text[insertion_point:]
    xml_path.write_text(updated, encoding="utf-8")
    return True, f"Inserted class entry for {full_class}"


