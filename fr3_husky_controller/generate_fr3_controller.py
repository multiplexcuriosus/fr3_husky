#!/usr/bin/env python3
"""
generate_fr3_controller.py

Generates FR3 controller boilerplate:
  - HPP: include/fr3_husky_controller/<snake>.hpp
  - CPP: src/<snake>.cpp

Also patches:
  - CMakeLists.txt        : adds .cpp to add_library block
  - fr3_husky_plugin.xml  : adds class entry
  - config/fr3_ros_controllers.yaml : adds left_<name>, right_<name>, dual_<name> entries
                                      with manipulator_command_interface = <control_mode>

Usage:
  python3 generate_fr3_controller.py MyNewController --control_mode effort
  python3 generate_fr3_controller.py MyNewController --control_mode position --force
"""

