STATE_INTERFACES_FOR_MODE = {
    "position": "        - position\n        - velocity",
    "velocity": "        - position\n        - velocity",
    "effort":   "        - position\n        - velocity\n        - effort",
}


def make_yaml_type_block(ctrl_name: str, class_name: str) -> str:
    return (
        f"\n      {ctrl_name}:\n"
        f"        type: fr3_husky_controller/{class_name}\n"
    )


def make_yaml_param_block(ctrl_name: str, robot_names: list, joints_str: str, control_mode: str) -> str:
    robot_names_str = '["' + '", "'.join(robot_names) + '"]'
    state_ifaces = STATE_INTERFACES_FOR_MODE.get(control_mode, STATE_INTERFACES_FOR_MODE["effort"])
    return (
        f"\n  {ctrl_name}:\n"
        f"    ros__parameters:\n"
        f"      update_rate: 1000  # Hz\n"
        f"      robot_name: {robot_names_str}\n"
        f"      manipulator_joints:\n"
        f"{joints_str}\n"
        f"      manipulator_state_interfaces:\n"
        f"{state_ifaces}\n"
        f"      manipulator_command_interface: {control_mode}\n"
    )


def patch_yaml(yaml_path: Path, snake: str, class_name: str, control_mode: str) -> Tuple[bool, str]:
    """
    1. Add type declarations under controller_manager/ros__parameters.
    2. Append left/right/dual parameter sections at the end.
    """
    text = yaml_path.read_text(encoding="utf-8")

    left_name  = f"left_{snake}"
    right_name = f"right_{snake}"
    dual_name  = f"dual_{snake}"

    if left_name in text:
        return False, f"Already present: {left_name}"

    # --- Part 1: insert type declarations ---
    # Type lines inside controller_manager are indented 8 spaces:
    #   "        type: fr3_husky_controller/..."
    # Find the last such line and append our entries after it.
    type_pattern = re.compile(r"^ {8}type: fr3_husky_controller/\S+", re.MULTILINE)
    matches = list(type_pattern.finditer(text))

    new_type_entries = (
        f"\n\n      {left_name}:\n        type: fr3_husky_controller/{class_name}"
        f"\n\n      {right_name}:\n        type: fr3_husky_controller/{class_name}"
        f"\n\n      {dual_name}:\n        type: fr3_husky_controller/{class_name}"
    )

    if matches:
        last_match = matches[-1]
        # Advance to end of the line
        line_end = text.find("\n", last_match.end())
        if line_end == -1:
            line_end = len(text)
        else:
            line_end += 1  # include the newline
        text = text[:line_end] + new_type_entries + "\n" + text[line_end:]
    else:
        # Fallback: append after controller_manager/ros__parameters: line
        cm_pattern = re.search(r"ros__parameters:\n", text)
        if cm_pattern:
            insert_pos = cm_pattern.end()
            text = text[:insert_pos] + new_type_entries + "\n" + text[insert_pos:]
        else:
            text += "\n" + new_type_entries + "\n"

    # --- Part 2: append parameter sections ---
    left_joints  = SINGLE_ARM_JOINTS_TEMPLATE.format(side="left")
    right_joints = SINGLE_ARM_JOINTS_TEMPLATE.format(side="right")
    dual_joints  = DUAL_ARM_JOINTS_TEMPLATE

    left_block  = make_yaml_param_block(left_name,  ["left"],          left_joints,  control_mode)
    right_block = make_yaml_param_block(right_name, ["right"],         right_joints, control_mode)
    dual_block  = make_yaml_param_block(dual_name,  ["left", "right"], dual_joints,  control_mode)

    text = text.rstrip("\n") + "\n" + left_block + right_block + dual_block

    yaml_path.write_text(text, encoding="utf-8")
    return True, f"Added left/right/dual entries for {snake}"


