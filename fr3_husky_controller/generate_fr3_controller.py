def patch_cmakelists(cmake_path: Path, new_cpp_rel: str) -> Tuple[bool, str]:
    """Add new_cpp_rel to the add_library block. Deduped."""
    text = cmake_path.read_text(encoding="utf-8")
    if new_cpp_rel in text:
        return False, f"Already present: {new_cpp_rel}"

    lines = text.splitlines(True)

    def find_blocks():
        blocks = []
        i = 0
        while i < len(lines):
            if re.match(r"^\s*add_(library|executable)\s*\(", lines[i]):
                start = i
                depth = lines[i].count("(") - lines[i].count(")")
                i += 1
                while i < len(lines) and depth > 0:
                    depth += lines[i].count("(") - lines[i].count(")")
                    i += 1
                blocks.append((start, i))
            else:
                i += 1
        return blocks

    blocks = find_blocks()

    # Pick the add_library block containing ${PROJECT_NAME}; fall back to first block
    chosen = None
    for s, e in blocks:
        if "${PROJECT_NAME}" in "".join(lines[s:e]):
            chosen = (s, e)
            break
    if chosen is None and blocks:
        chosen = blocks[0]

    if chosen is None:
        hint = f"\n# TODO: add {new_cpp_rel} to your add_library target\n"
        cmake_path.write_text(text + hint, encoding="utf-8")
        return True, "No add_library block found; appended hint."

    s, e = chosen

    # Find last closing paren line
    insert_at = None
    for j in range(e - 1, s - 1, -1):
        if ")" in lines[j]:
            insert_at = j
            break
    if insert_at is None:
        insert_at = e

    # Detect indent from existing src/ lines
    indent = "  "
    for j in range(s, e):
        if "src/" in lines[j]:
            m = re.match(r"^(\s+)\S", lines[j])
            if m:
                indent = m.group(1)
                break

    lines.insert(insert_at, f"{indent}{new_cpp_rel}\n")
    cmake_path.write_text("".join(lines), encoding="utf-8")
    return True, f"Inserted into add_library: {new_cpp_rel}"


